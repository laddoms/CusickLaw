			<h3 style="text-align:center;">Terms of Tenancy</h3>
			<h4>Tenancy Based on a Lease</h4>
			<p>One kind of tenancy is based on a written lease. When a tenant signs a lease with a landlord, the tenant agrees that the 
			   tenancy will last for a certain amount of time, usually one year. 
			</p>
			<p>During that time, the monthly rent will stay the same and the landlord cannot end the tenancy (evict) unless the tenant fails to 
			   satisfy the conditions agreed upon in the lease. On the other hand, the tenant is committed to paying rent for the term of the lease.
			</p>
			<h4>Tenancy at Will</h4>
			<p>The other kind of tenancy is called a tenancy-at-will, which means that the lease agreement lasts for as long as both parties want
			   to do business with each other. Sometimes there is no written agreement at all in a tenancy-at-will, but often the tenant is asked 
			   to sign a form that says “Rental Agreement” or “Tenancy-at-Will” at the top. This form should include the amount of the monthly rent 
			   and basic rules.
			</p>
			<h3 style="text-align:center;">Eviction</h3>
			<p>Generally, a landlord cannot simply take possession of the rental property, physically remove the tenant or their personal property, 
			   or change the locks. Depending on the reason for eviction, a landlord must provide either a 14-Day or 30-day Notice to Quit 
			   to the Tenant. A landlord must then file a civil action in court, and obtain a judgment from the court that specifies the date 
			   that the tenant must leave the rental property with their belongings. If the tenant does not voluntarily leave on the date 
			   specified by the court, a landlord must arrange for a Sheriff or Constable to serve an executed Judgment on the tenant ordering 
			   them to leave.
			</p>



